package com.mx.xozello.model;

public class Apple {
	String color;
	int weight;

	public Apple() {
	}

	public Apple(String color, int weight) {
		super();
		this.color = color;
		this.weight = weight;

	}

	public String getColor() {
		return color;
	}

	public void setColor(String color) {
		this.color = color;
	}

	public int getWeight() {
		return weight;
	}

	public void setWeight(int weight) {
		this.weight = weight;
	}

}
